#funcion sin parametro
def nombreFuncion():
    print("mensaje")
    print("\t")

nombreFuncion()


#funcion con parametro
def nombreFuncionConParametro(parametro):
    print("este es el parametro: "+ parametro)

nombreFuncionConParametro("este es el argumento del parametro ")

#funcion multiples parametros
def nombreFuncionConMultiplesParametros(parametro1, parametro2):
    print("estos son los valores de los multiples parametros: "+ parametro1 +" y "+ parametro2)

nombreFuncionConMultiplesParametros("este es el argumento 1", "este es el parametro2")
